function mynotify() {
    $.ajax({
    url:'<?=base_url();?>Menu/notipopup',
    type: "POST",
    cache: false,
    success: function (result){
        var res = result;
        if(res!=0){
            $('#alartpopup').modal('show');
            $("#alsms").html(result);
        }
    }
    });
}

function read(id) {
  document.getElementById(id).style.display = "none";

$.ajax({
url:'<?=base_url();?>Menu/readnotify',
type: "POST",
data: {
id: id
},
cache: false,
success: function b(result){
$("#cinfo").html(result);
}
});
}

 function functionAlert(msg, myYes) {
    var confirmBox = $("#confirm");
    confirmBox.find(".message").text(msg);
    confirmBox.find(".yes").unbind().click(function() {
       confirmBox.hide();
    });
    confirmBox.find(".yes").click(myYes);
    confirmBox.show();
 }

</script>

